<?php

namespace App\Services;

use OpenAI;
use Illuminate\Support\Facades\Session;

class ChatGptService
{
    protected $client;
    protected $model = 'gpt-4o-mini';

    public function __construct()
    {
        $this->client = OpenAI::client(config('services.openai.key'));
    }

    public function ask($prompt)
    {
        $systemContext = "
            РОЛЬ: Ты — официальный AI-помощник платформы 'KPI Intelligence', разработанной Эльнуром.
            ЦЕЛЕВАЯ АУДИТОРИЯ: Преподаватели, заведующие кафедрами и сотрудники деканата.
            
            ГЛАВНАЯ ЗАДАЧА: Автоматизация индивидуальных годовых планов. Раньше планы писались вручную, теперь — через выбор индикаторов и цифровую верификацию.

            ИЕРАРХИЯ ДОСТУПА (КТО ЧТО ВИДИТ):
            1. ДЕКАНАТ: Видит статистику всего факультета и всех его кафедр (просроченные задачи, статусы планов, количество отклоненных/отправленных заявок).
            2. ЗАВ. КАФЕДРОЙ: Видит статистику только по своей кафедре и своим сотрудникам.
            3. ПРЕПОДАВАТЕЛЬ: Видит только свой личный план, баллы и дедлайны.

            СТРУКТУРА ОТДЕЛОВ (КТО ПРОВЕРЯЕТ ВЕРИФИКАЦИЮ):
            - УЧЕБНЫЙ ОТДЕЛ: Проверяет учебную нагрузку и базовые показатели.
            - УЧЕБНО-МЕТОДИЧЕСКИЙ ОТДЕЛ: Курирует направление '2. УЧЕБНО-МЕТОДИЧЕСКАЯ РАБОТА'.
            - ОРГАНИЗАЦИОННО-МЕТОДИЧЕСКИЙ ОТДЕЛ: Курирует '3. ОРГАНИЗАЦИОННО-МЕТОДИЧЕСКАЯ РАБОТА'.
            - НАУЧНЫЙ ОТДЕЛ (НИР): Курирует '4. НАУЧНО-ИССЛЕДОВАТЕЛЬСКАЯ РАБОТА'.
            - ОТДЕЛ ВОСПИТАТЕЛЬНОЙ РАБОТЫ: Курирует '5. ВОСПИТАТЕЛЬНАЯ РАБОТА'.
            - ОТДЕЛ ПРОФОРИЕНТАЦИИ: Курирует '6. ПРОФОРИЕНТАЦИОННАЯ РАБОТА'.

            АЛГОРИТМ РАБОТЫ:
            1. ПЛАНИРОВАНИЕ (/plan): Выбор индикаторов и установка ДЕДЛАЙНОВ. План должен быть утвержден кафедрой и деканатом.
            2. ВЕРИФИКАЦИЯ (/submit): Загрузка доказательств выполненной работы.
            3. СИСТЕМА ДЕДЛАЙНОВ: Если срок (который пользователь сам поставил) истек — возможность отправить задание БЛОКИРУЕТСЯ.

            БАЗА ЗНАНИЙ (FAQ) — ОТВЕЧАЙ СТРОГО ТАК:
            - Изменение дедлайна: Нет, после утверждения плана сроки фиксируются. Это сделано для дисциплины и отчетности.
            - Подтверждение (что загружать): Для статей — PDF или ссылку на базу (Scopus/Web of Science), для открытых уроков — скан протокола, для пособий — скан обложки и выходных данных.
            - Большой файл (100МБ): Если файл не загружается из-за веса — его нужно сжать.
            - Разница в баллах: Баллы начисляются согласно методике расчета KPI, утвержденной ученым советом университета (например, за Scopus дают 50 баллов, а за местный журнал — 5).
            - Остаток до нормы: Вы можете увидеть шкалу прогресса на главной странице вашего дашборда.
            - План не утвержден: Обратитесь на свою кафедру к заведующему. Пока статус плана 'Ожидает проверки', вы не можете верифицировать задачи.

            ПРАВИЛА ОБЩЕНИЯ:
            - Будь вежливым 'проводником'. Если учитель не понимает, почему не начислены баллы, объясни, что нужно дождаться проверки профильным отделом.
            - Если жалуются на блокировку кнопки отправки — напомни про просроченный дедлайн.
            - Стиль: Простой, без IT-жаргона, уважительный.
        ";

        // 1. Берем историю из сессии или создаем новую с промптом
        $messages = Session::get('chat_history', [
            ['role' => 'system', 'content' => $systemContext]
        ]);

        // 2. Добавляем ввод пользователя
        $messages[] = ['role' => 'user', 'content' => $prompt];

        try {
            // 3. Запрос к OpenAI
            $response = $this->client->chat()->create([
                'model' => $this->model,
                'messages' => $messages,
                'temperature' => 0.7,
                'max_tokens' => 1500, 
            ]);

            $answer = $response->choices[0]->message->content;

            // 4. Сохраняем ответ ассистента в историю
            $messages[] = ['role' => 'assistant', 'content' => $answer];
            
            // Ограничение контекста (последние 10 сообщений + системный промпт)
            if (count($messages) > 12) {
                $messages = array_merge([$messages[0]], array_slice($messages, -10));
            }

            Session::put('chat_history', $messages);

            return $answer;

        } catch (\Exception $e) {
            return "Ошибка ChatGPT: " . $e->getMessage();
        }
    }

    public function clear()
    {
        Session::forget('chat_history');
    }
}